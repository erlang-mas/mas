%%%-----------------------------------------------------------------------------
%%% @doc MAS application.
%%% @end
%%%-----------------------------------------------------------------------------

-module(mas_app).

-behaviour(application).

%%% Application callbacks
-export([start/2, stop/1]).

%%%=============================================================================
%%% Application callbacks
%%%=============================================================================

%%------------------------------------------------------------------------------
%% @private
%%------------------------------------------------------------------------------
start(normal, _Args) ->
    Config = mas_config:get_all(),
    mas_logger:info("Starting MAS engine with configuration: ~p", [Config]),
    mas_sup:start_link().

%%------------------------------------------------------------------------------
%% @private
%%------------------------------------------------------------------------------
stop(_State) ->
    ok.
