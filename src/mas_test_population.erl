%%%-----------------------------------------------------------------------------
%%% @doc Population behaviour for testing purposes.
%%% @end
%%%-----------------------------------------------------------------------------

-module(mas_test_population).

-behaviour(mas_population).

%%% API
-export([]).

%%% Population callbacks
-export([init_agent/1,
         init/2,
         step/2,
         metrics/2,
         terminate/2]).

-record(state, {}).

%%%=============================================================================
%%% Population callbacks
%%%=============================================================================

%%------------------------------------------------------------------------------
%% @private
%%------------------------------------------------------------------------------
init_agent(_SP) ->
    rand:uniform().

%%------------------------------------------------------------------------------
%% @private
%%------------------------------------------------------------------------------
init(Agents, _SP) ->
    {Agents, #state{}}.

%%------------------------------------------------------------------------------
%% @private
%%------------------------------------------------------------------------------
step(Agents, State) ->
    {Agents, [], State}.

%%------------------------------------------------------------------------------
%% @private
%%------------------------------------------------------------------------------
metrics(_Agents, State) ->
    {[], State}.

%%------------------------------------------------------------------------------
%% @private
%%------------------------------------------------------------------------------
terminate(_Agents, _State) ->
    ok.
